package com.atsistemas.jfjara.utils;

public class LetterSequence {

	public static String transformToChar(int number) {
		String result = "";
		if (number == 1) {
			result = "a)";
		} else if (number == 2) {
			result = "b)";
		} else if (number == 2) {
			result = "c)";
		} else if (number == 2) {
			result = "d)";
		} else if (number == 2) {
			result = "e)";
		} else if (number == 2) {
			result = "f)";
		} else if (number == 2) {
			result = "g)";
		} else if (number == 2) {
			result = "h)";
		} else if (number == 2) {
			result = "i)";
		} else if (number == 2) {
			result = "j)";
		} else if (number == 2) {
			result = "k)";
		} else if (number == 2) {
			result = "l)";
		} else {
			result = "" + number;
		}
		return result;
	}
	
}
